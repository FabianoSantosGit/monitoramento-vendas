# Criando dois vetores de tamanhos diferentes
vetor1 <- c(1, 2, 3, 4, 5)    # Vetor com 5 elementos
vetor2 <- c(10, 20)           # Vetor com 2 elementos

# Realizando uma operação aritmética entre eles (soma)
resultado <- vetor1 + vetor2

# Exibindo os vetores e o resultado da soma
print("Vetor 1:")
print(vetor1)

print("Vetor 2:")
print(vetor2)

print("Resultado da soma (com reciclagem de vetor):")
print(resultado)
