# Vetor x
x <- c(50, 10, 5, 1, 2, 3, 11, 25, 30, 35, 40, 4, 42)

# Descobrindo a ordem dos índices
ordem_indices <- order(x)
print("Ordem dos índices para classificar o vetor:")
print(ordem_indices)

# Organizando o vetor em ordem crescente
x_ordenado <- sort(x)
print("Vetor em ordem crescente:")
print(x_ordenado)