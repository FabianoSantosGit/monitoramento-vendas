# Criando o vetor idade com os valores fornecidos
idade <- c(15, 22, 30, 17, 25, 18)

# Usando operadores lógicos para identificar quem é maior ou igual a 18 anos
pode_votar <- idade >= 18

# Imprimindo o resultado mostrando TRUE para quem pode votar e FALSE para quem não pode
print("Verificação de quem pode votar (TRUE = pode votar, FALSE = não pode votar):")
print(pode_votar)
