# Criando um vetor com 2 números inteiros positivos
vetor_numeros <- c(16, 64)

# Calculando a raiz quadrada de cada elemento do vetor
raizes_quadradas <- sqrt(vetor_numeros)
print("Raiz quadrada de cada elemento do vetor:")
print(raizes_quadradas)

# Aplicando a função de logaritmo (base natural) aos resultados das raízes quadradas
logaritmo_resultado <- log(raizes_quadradas)
print("Logaritmo natural das raízes quadradas:")
print(logaritmo_resultado)
